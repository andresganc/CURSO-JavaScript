
function suma() {

    var num1 = 5;
    var num2 = 3;

    var resultado = ( parseInt(num1) + parseInt(num2) );

    console.log("El resultado de la suma es :", resultado);

}

function resta() {

    var num1 = 8;
    var num2 = 2;

    var resultado = ( parseInt(num1) - parseInt(num2) );

    console.log("El resultado de la resta es :", resultado);

}


suma();


